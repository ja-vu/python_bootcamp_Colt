# What is OOP?
# OOP is a method of programming that attempts to model some process or thing in the world as
# a class or object

# class - a blueprint for objects. Classes can contain methods (function) and attributes (similar to keys in a dict)
# instance - Objects that are constructed from a class blueprint that their class's method and properties

# Why OOP?
# encapsulation - grouping of public and private attributes and methods in a programmatic class,
# making abstraction possible
# abstraction - exposing only "relevant" data in a class interface, hiding private attributes
# and methods (aka the inner workings) from users


